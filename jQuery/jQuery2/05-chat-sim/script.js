$(document).ready(() => {
  const botReplies = ['Интересно!', 'Понял, спасибо.', 'Давай продолжим.', 'Хм, надо подумать...', 'Отличная идея!', 'Можешь рассказать подробнее?', 'Я с тобой согласен.', 'Это звучит здорово!', 'Спасибо за информацию!', 'Давай обсудим это.', 'Я не уверен, что понимаю.', 'Можешь объяснить по-другому?', 'Это важно.', 'Я ценю твою точку зрения.', 'Давай попробуем другой подход.', 'Это звучит сложно, но интересно.', 'Я рад, что ты поделился этим.', 'Давай посмотрим на это с другой стороны.', 'Спасибо за твою честность!', 'Это заставляет меня задуматься.', 'Я ценю твою открытость.', 'Давай продолжим диалог.', 'Это звучит как вызов!', 'Спасибо за твою поддержку!', 'Я рад, что мы можем обсудить это.', 'Давай найдем решение вместе.', 'Это звучит как хорошая идея!', 'Спасибо за твою помощь!',  'Давай продолжим обсуждение.'];

  function addMessage(text, isUser) {
    const $msg = $(`<div class="msg ${isUser ? 'user' : 'bot'}">${text}</div>`);
    $('#chat-window').append($msg).scrollTop($('#chat-window')[0].scrollHeight);
  }

  $('#chat-input').on('keydown', function(e) {
    if (e.key === 'Enter' && $(this).val().trim()) {
      const text = $(this).val().trim();
      addMessage(text, true);
      $(this).val('');
      setTimeout(() => {
        const reply = botReplies[Math.floor(Math.random() * botReplies.length)];
        addMessage(reply, false);
      }, 2000);
    }
  });
});